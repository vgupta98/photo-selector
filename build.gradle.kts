// The app reads its own version and the update-feed URL at runtime through a generated BuildConfig, so
// the `version` above stays the single source of truth instead of a hand-synced runtime constant. The
// feed is a static JSON hosted in the Homebrew tap repo (already write-access wired in release.yml);
// editing it there is the kill switch / staged-rollout control. A tiny codegen task — no plugin.
val updateManifestUrl = "https://raw.githubusercontent.com/vgupta98/homebrew-tap/main/update-manifest.json"
val generateBuildConfig by tasks.registering {
    val outputDir = layout.buildDirectory.dir("generated/buildconfig")
    val versionValue = project.version.toString()
    inputs.property("version", versionValue)
    inputs.property("manifestUrl", updateManifestUrl)
    outputs.dir(outputDir)
    doLast {
        val pkgDir = outputDir.get().asFile.resolve("com/vishalgupta/photoselector")
        pkgDir.mkdirs()
        pkgDir.resolve("BuildConfig.kt").writeText(
            buildString {
                appendLine("// Generated by the generateBuildConfig Gradle task from the project version. Do not edit.")
                appendLine("package com.vishalgupta.photoselector")
                appendLine()
                appendLine("object BuildConfig {")
                appendLine("    const val VERSION: String = \"$versionValue\"")
                appendLine("    const val UPDATE_MANIFEST_URL: String = \"$updateManifestUrl\"")
                appendLine("}")
            },
        )
    }
}

kotlin.sourceSets["main"].kotlin.srcDir(generateBuildConfig)

